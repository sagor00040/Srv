// Cache cleanup utility
function cleanupCache() {
  caches.open(DYNAMIC_CACHE)
    .then((cache) => {
      cache.keys()
        .then((requests) => {
          if (requests.length > 50) {
            // Remove oldest entries if cache gets too large
            cache.delete(requests[0]);
          }
        });
    });
}

